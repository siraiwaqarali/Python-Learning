# and operator - returns true if all conditions are true else false
# or operator - returns true if any of the condition is true esle false if all are false

# and operator
name = "Waqar"
age = 19
if name=="Waqar" and age==19:
    print("Condtion True")
else: 
    print("Conditon False")

# or operator
name = "Waqar"
age = 19
